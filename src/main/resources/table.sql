comment on column showcle_member_social.provider is 'SNS 연동 provider ( GOOGLE, KAKAO, APPLE )';
comment on column showcle_member_social.provider_user_id is 'provider 별 sns 고유 id';
comment on column showcle_member_social.email is '이메일';
comment on column showcle_member_social.created_at is '생성일시';

create index showcle_member_social_idx on showcle_member_social(provider, provider_user_id);


-- ============================================================
-- 첨부파일 공통 테이블
-- ============================================================
create table showcle_file
(
    idx	bigint generated by default as identity,
    owner_idx bigint not null,
    type varchar(30) not null,
    file_type varchar(50),
    file_size varchar(200),
    save_file_name varchar(200),
    real_file_name varchar(200),
    save_path varchar(200),
    web_path varchar(200),
    file_key varchar(200),
    discarded smallint default 0,
    created_at timestamp,
    created_by varchar(100),
    primary key(idx)
);

create index showcle_file_owner_idx on showcle_file(owner_idx);
create index showcle_file_type on showcle_file(type);

