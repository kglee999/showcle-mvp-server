-- ============================================================
-- 회원 SNS 연동 테이블
-- ============================================================
CREATE TABLE showcle_member_social (
    idx bigint generated by default as identity,
    member_idx bigint not null,
    provider varchar(20) not null,
    provider_user_id varchar(255) not null,
    email varchar(100),
    created_at timestamp,
    primary key (idx)
